package com.sreenu.daimond;

public class Daimond {
	
	
	private int size;
	private String color;
	private int number;
	private Daimond right;
	private Daimond left;
	private Daimond top;
	private Daimond bottom;
	
	
	
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public Daimond getRight() {
		return right;
	}
	public void setRight(Daimond right) {
		this.right = right;
	}
	public Daimond getLeft() {
		return left;
	}
	public void setLeft(Daimond left) {
		this.left = left;
	}
	public Daimond getTop() {
		return top;
	}
	public void setTop(Daimond top) {
		this.top = top;
	}
	public Daimond getBottom() {
		return bottom;
	}
	public void setBottom(Daimond bottom) {
		this.bottom = bottom;
	}

}
